# --- Consolidation and Cleanup Functions ---

def consolidate_readmes_one_level_deep(root_dir, output_file):
    root_path = pathlib.Path(root_dir)
    output_path = pathlib.Path(output_file)
    
    found_count = 0

    with open(output_path, 'w', encoding='utf-8') as outfile:
        for sub_dir_path in root_path.iterdir():
            if sub_dir_path.is_dir():
                readme_path = sub_dir_path / 'README.md'
                if not readme_path.exists():
                    readme_path = sub_dir_path / 'readme.md'
                
                if readme_path.is_file():
                    found_count += 1
                    
                    relative_folder = sub_dir_path.name
                    outfile.write("\n\n\n")
                    outfile.write(f"# Source: {relative_folder}\n")
                    outfile.write("-" * 40 + "\n\n") # This line is specifically for the separator you want to clean
                    
                    with open(readme_path, 'r', encoding='utf-8') as infile:
                        outfile.write(infile.read())
                    
                    print(f"Added: {readme_path}")
                else:
                    print(f"Skipping directory '{sub_dir_path.name}': No 'README.md' or 'readme.md' found directly inside it.")

    print(f"\nDone! Consolidated {found_count} files into '{output_path.absolute()}'.")
    return output_path # Return the path for the next step

def cleanup_consolidated_file(file_path):
    """
    Cleans up the consolidated markdown file by applying a set of regex patterns.

    Args:
        file_path (pathlib.Path): The path to the consolidated markdown file.
    """
    print(f"\nStarting cleanup of '{file_path}'...")
    
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            content = f.read()
        
        # Regex patterns to apply (Order matters))

        # 1. Remove '## Build instructions' to '## Canary string' section
        #    (The exact regex you provided for the previous VS Code task)
        print("Applying regex: Removing '## Build instructions' and '## Canary string' sections...")
        # content = re.sub(r'^## Build instructions[\s\S\n]*?^## Canary string[\s\S]*?(?=^# Source:.*|$)', '', content, flags=re.MULTILINE)
        content = re.sub(r'^## Build instructions[\s\S\n]*?^## Canary string[\s\S]*?(?=^# Source:.*)', '', content, flags=re.MULTILINE)

        # 2. Remove '# Source: .*\n----------------------------------------\n\n'
        #    This is the header separator generated by the consolidation script
        print("Applying regex: Removing '# Source: ...' header separator...")
        content = re.sub(r'^# Source: .*\n-+\n\n', '', content, flags=re.MULTILINE)
        
        # 3. Remove '\n## Challenge Details\n'
        print("Applying regex: Removing '## Challenge Details' header...")
        content = re.sub(r'\n## Challenge Details\n', '\n', content) # Replace with just a newline to merge content above/below

        # Optional: Clean up excessive blank lines (e.g., more than 2 consecutive newlines)
        print("Applying regex: Cleaning up excessive blank lines...")
        content = re.sub(r'\n{3,}', '\n\n', content)
        
        # Write the cleaned content back to the file
        with open(file_path, 'w', encoding='utf-8') as f:
            f.write(content.strip()) # strip final leading/trailing whitespace

        print(f"Cleanup complete for '{file_path}'.")
        return content # Return cleaned content for direct processing
    except FileNotFoundError:
        print(f"Error during cleanup: File not found at '{file_path}'")
        return None
    except Exception as e:
        print(f"An error occurred during cleanup: {e}")
        return None

# --- Benchmark Parsing Functions ---

def parse_readme_to_json(readme_content):
    """
    Parses the README content into a list of JSON objects, where each object
    represents a benchmark.
    """
    benchmarks = []
    
    # Remove the "--- START OF FILE ..." header if present (though cleanup should handle most of this)
    readme_content = re.sub(r'--- START OF FILE.*---', '', readme_content, flags=re.IGNORECASE | re.DOTALL).strip()

    # Split the content by the main benchmark header (e.g., "# XBEN-001-24 ...")
    # We use a non-capturing group (?:...) to split but not include the delimiter
    parts = re.split(r'(^# XBEN-\d{3}-\d{2} .*?)', readme_content, flags=re.MULTILINE)
    
    # Process the parts to combine headers with their content
    current_benchmark_raw = ""
    for part in parts:
        if not part.strip(): # Skip empty parts from split
            continue
        
        if part.startswith('# XBEN-'):
            if current_benchmark_raw: # If we have accumulated a benchmark, process it
                parsed_benchmark = parse_single_benchmark(current_benchmark_raw)
                if parsed_benchmark:
                    benchmarks.append(parsed_benchmark)
            current_benchmark_raw = part # Start a new benchmark with this header
        else:
            current_benchmark_raw += part # Append content to the current benchmark

    if current_benchmark_raw: # Process the last accumulated benchmark
        parsed_benchmark = parse_single_benchmark(current_benchmark_raw)
        if parsed_benchmark:
            benchmarks.append(parsed_benchmark)

    return benchmarks

def parse_single_benchmark(benchmark_text):
    """
    Parses a single benchmark's text into a dictionary.
    """
    benchmark_data = {}

    # 1. ID and Name
    id_name_match = re.match(r'^# (XBEN-\d{3}-\d{2}) (.+)', benchmark_text, re.MULTILINE)
    if id_name_match:
        benchmark_data['id'] = id_name_match.group(1)
        benchmark_data['name'] = id_name_match.group(2).strip()
    else:
        print(f"Warning: Could not parse ID and Name for a benchmark starting with: {benchmark_text.splitlines()[0] if benchmark_text else 'EMPTY TEXT'}")
        return None

    # 2. Description
    # Matches content between '### Description' and the next '###' heading or end of string
    description_match = re.search(r'### Description\n([\s\S]*?)(?=\n### |\Z)', benchmark_text)
    if description_match:
        benchmark_data['description'] = description_match.group(1).strip()
    else:
        benchmark_data['description'] = "" # Description might be optional or empty

    # 3. Vulnerability Type and Category (can be multiple blocks)
    vulnerability_matches = re.findall(
        r'### Vulnerability Type and Category\n'
        r'- \*\*Type:\*\* (.*?)\n'
        r'- \*\*Category:\*\* (.*?)(?=\n### |\Z)',
        benchmark_text,
        re.DOTALL
    )
    benchmark_data['vulnerabilities'] = []
    for v_type, v_category in vulnerability_matches:
        benchmark_data['vulnerabilities'].append({
            'type': v_type.strip(),
            'category': v_category.strip()
        })

    # 4. Difficulty
    difficulty_match = re.search(r'### Difficulty\n(Medium|Easy|Hard|Unknown)', benchmark_text)
    if difficulty_match:
        benchmark_data['difficulty'] = difficulty_match.group(1).strip()
    else:
        benchmark_data['difficulty'] = "Unknown" # Default if not found

    return benchmark_data

# --- Aggregation Function ---

def aggregate_vulnerabilities(benchmarks_data):
    """
    Aggregates vulnerability data from a list of parsed benchmark dictionaries.
    """
    aggregated = defaultdict(lambda: {'ids': []})

    for benchmark in benchmarks_data:
        benchmark_id = benchmark['id']
        
        for vuln in benchmark['vulnerabilities']:
            vuln_type = vuln['type']
            category = vuln['category']
            
            key = (vuln_type, category)
            
            if key not in aggregated:
                aggregated[key]['type'] = vuln_type
                aggregated[key]['category'] = category
            
            if benchmark_id not in aggregated[key]['ids']:
                aggregated[key]['ids'].append(benchmark_id)

    result = []
    for (vuln_type, category), data in aggregated.items():
        result.append({
            'type': vuln_type,
            'category': category,
            'ids': data['ids'],
            'count': len(data['ids'])
        })

    result = sorted(result, key=lambda x: x['count'], reverse=True)
    return result

